    *    HISTÓRIA DO .NET
    *       A Microsoft começou a trabalhar no C# no final dos anos 90, tendo sua primeira versão do framework em 2002, tendo como objetivo competir com o Java. 
    *       O conceito de multiplataforma era estrondoso na época, e a Microsoft trabalhou em melhorias e implementações do Java em sua plataforma, mas foi impedida pela Sun.
    *       Isso motivou a Microsoft a desenvolver um ecossistema integrado ao Windows, ue fosse fácil para desenvolver aplicativos desktop e web, e que fosse amarrado apenas ao Windows, aumentando assim a sua relevância
    *    
    *    .NET FRAMEWORK (Legado) e.NET
    *       O .NET Framework só funciona no Windows, já o .NET (ou .NET Core), é a versão atual, e multiplataforma.
    *       A partir do .NET 5, a palavra Core foi removida, para enfatizar que esta seria a implementação principal do .NET no futuro.
    *    
    *    
    *    O COMPILADOR .NET E SEU FUNCIONAMENTO
    *       - Linguagem de alto nível: A linguagem que entendemos e escrevemos nosso código fonte.
    *       - Linguagem de baixo nível: A linguagem que a máquina entende. Possui pouca abstração, sendo difícil de entender.
    *    
    *    O que é um compilador?
    *       É um programa que realiza a conversão de linguagem de alto nível para baixo nível (binário).
    *    
    *    FASES DE UM COMPILADOR
    *       Linguagem de Alto Nível -> 
    *           Análise Léxica ->
    *           Análise Sintática ->
    *           Gerador de código intermediário ->
    *           Otimizador de código ->
    *           Gerador de código ->
    *       Código de máquina
    *    
    *       Em meio a esse processo, também atuam a Tabela de Símbolos e o Manipulador de Erros.
    *    
    *    COMPILADOR DO .NET
    *       C# code -> C# compiler -> IL(Intermediary Language) code (.exe/.dll) -> JIT(Just In Time) compiler -> Native code
    *                   Compile Time                    |                   Runtime
    *    
    *    COMPILADOR E TRANSPILADOR
    *       Compilador: Como visto anteriormente, é um programa que realiza a conversão de linguagem de alto nível para baixo nível. Ex.: C#, Java.
    *       Transpilador: É a conversão de uma linguagem ou implementação para outra. A sua saída permanece em linguagem de alto nível. Ex.: Typescript para Javascript
    *    
    *    
    *    NEM TODA LINGUAGEM É COMPILADA
    *       Linguagem compilada: São linguagens onde o código fonte é traduzido para o código de máquina.
    *       Linguagem interpretada: São linguagens que fazem a leitura e interpretação diretamente do código fonte. Ex.: Javascript e PHP
