    *    As ferramentas que podem ser utilizadas com o .NET
    *        - Visual Studio
    *        - Visual Studio Code
    *        - Command Line Interface
    *    
    *    Em resumo, o .NET é uma plataorma que fornece as soluções para diversas aplicações que uma pessoa deseja construir.
