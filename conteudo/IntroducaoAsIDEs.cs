/*  
*   O que é uma IDE?
*       Uma IDE (Integrated Development Environment), ou ambiente de desenvolvimento integrado, é um software que facilita e integra diversas facilidades para a escrita e depuração de um software
*   
*   Visual Studio
*       É a principal IDE para o .NET, com suporte para o C#, C++, Python, Node.js, Unity e mobile.
*       Vantagens:
*           - Versão gratuita (Community).
*           - Debug rico em detalhes.
*           - Disponível para Windows e Mac.
*       Desvantagens:
*           - Não disponível para Linux.
*           - Performance. Exige muitos recursos da máquina.
*   
*   Visual Studio Code
*       O VS Code é um editor de texto usado para facilitar o desenvolvimento de diversas linguagens.
*       Vantagens:
*           - Totalmente gratuito.
*           - Possibilidade de instalar extensões.
*           - Disponível para Windows, Mac e Linux.
*           - Muito leve em performance.
*       Desvantagens:
*           - É necessário uma configuração inicial.
*           - Não muito intuitivo para algumas funcionalidades.
*   
*   Rider (JetBrains)
*       Vantagens:
*           - Rico em funcionalidades
*           - Forte em integração com o .NET
*           - Facilidade em trabalhar com Unity
*           - Recomendações de fatoração de código (ajuda muito)
*           - Atalhos e comandos que aumentam a produtividade
*       Desvantagens:
*           - Pago
*           - Performance. Exige muitos recursos da máquina.
*   
*   
*   Configurando o ambiente:
*       Downloads:
*           - .NET SDK (Source Development Kit). Para aplicações onde só irão rodar as aplicações, somente o Runtime é necessário.
*           - VS Code
*       
*       Para que o VS Code funcione como uma IDE, é necessário instalar uma extensão do C#.
*       - Abrir uma pasta vazia
*       - Abrir o terminal integrado do VS Code (Menu ou tecla de atalho).
*       - Executar comando CLI do .NET: dotnet new console (console é o tipo do projeto).
*       - Instalar extensão C#.
*       
*       O menu de pesquisa, serve tanto para buscar elementos, quanto para substituir o nome dos elementos encontrados.
*       
*/